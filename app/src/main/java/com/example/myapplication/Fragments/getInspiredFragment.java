    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        // Inflate the layout for this fragment
        View view = inflater.inflate(R.layout.fragment_getinspired, container, false);

        RecyclerView recyclerView = view.findViewById(R.id.twitterRecyclerView);

        //Create a timeline
        UserTimeline userTimeline = new UserTimeline.Builder().screenName("exercise_allday").build();
        TweetTimelineRecyclerViewAdapter adapter = new TweetTimelineRecyclerViewAdapter(getContext(), userTimeline);
        LinearLayoutManager llm = new LinearLayoutManager(getContext());

        recyclerView.setLayoutManager(llm);
        recyclerView.setAdapter(adapter);

        return view;
    }

